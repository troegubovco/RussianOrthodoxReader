def main():
    if len(sys.argv) != 3:
        print(f"Usage: {sys.argv[0]} <tsv_path> <conjugation_json_path>")
        sys.exit(1)

    tsv_path = Path(sys.argv[1])
    json_path = Path(sys.argv[2])

    if not tsv_path.exists():
        print(f"Error: TSV file not found: {tsv_path}")
        sys.exit(1)
    if not json_path.exists():
        print(f"Error: JSON file not found: {json_path}")
        sys.exit(1)

    # ── 1. Read TSV entries ──────────────────────────────────────────────────

    tsv_entries = []
    with open(tsv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for row in reader:
            word = row.get("word", "").strip()
            definition = row.get("definition", "").strip()
            source = row.get("source", "Церковнослав. словарь").strip()
            if word and definition:
                tsv_entries.append((word, definition, source))

    print(f"Read {len(tsv_entries)} entries from TSV")

    # ── 2. Read conjugation map ──────────────────────────────────────────────

    with open(json_path, "r", encoding="utf-8") as f:
        conjugation_map = json.load(f)

    print(f"Read {len(conjugation_map)} conjugation mappings from JSON")

    # ── 3. Merge all entries (Nyström + TSV), deduplicate by lowercase word ──

    all_entries = []
    seen = set()

    # Nyström entries first (higher priority)
    for word, definition, source in NYSTREM_ENTRIES:
        key = word.lower()
        if key not in seen:
            seen.add(key)
            all_entries.append((word, definition, source))

    # TSV entries
    for word, definition, source in tsv_entries:
        key = word.lower()
        if key not in seen:
            seen.add(key)
            all_entries.append((word, definition, source))

    print(f"Total unique entries: {len(all_entries)}")

    # ── 4. Build SQLite ──────────────────────────────────────────────────────

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    if OUT_FILE.exists():
        OUT_FILE.unlink()

    con = sqlite3.connect(OUT_FILE)
    cur = con.cursor()

    # entries table
    cur.executescript("""
        CREATE TABLE entries (
            rowid      INTEGER PRIMARY KEY,
            word       TEXT NOT NULL,
            definition TEXT NOT NULL,
            source     TEXT DEFAULT 'Нюстрем'
        );
        CREATE INDEX idx_word       ON entries(word COLLATE NOCASE);
        CREATE INDEX idx_word_lower ON entries(lower(word));
    """)

    cur.executemany(
        "INSERT INTO entries(word, definition, source) VALUES (?, ?, ?)",
        all_entries,
    )
    print(f"Inserted {len(all_entries)} entries")

    # conjugations table
    cur.execute("""
        CREATE TABLE conjugations (
            form  TEXT NOT NULL,
            lemma TEXT NOT NULL
        );
    """)
    cur.execute("CREATE INDEX idx_conj_form ON conjugations(form);")

    conj_rows = [(form, lemma) for form, lemma in conjugation_map.items()]
    cur.executemany(
        "INSERT INTO conjugations(form, lemma) VALUES (?, ?)",
        conj_rows,
    )
    print(f"Inserted {len(conj_rows)} conjugation mappings")

    con.commit()

    # Stats
    cur.execute("SELECT COUNT(*) FROM entries")
    entry_count = cur.fetchone()[0]
    cur.execute("SELECT COUNT(*) FROM conjugations")
    conj_count = cur.fetchone()[0]

    con.close()

    size_mb = OUT_FILE.stat().st_size / (1024 * 1024)
    print(f"\n✓ Built {OUT_FILE}")
    print(f"  {entry_count} dictionary entries")
    print(f"  {conj_count} conjugation mappings")
    print(f"  {size_mb:.2f} MB")


if __name__ == "__main__":
    main()
