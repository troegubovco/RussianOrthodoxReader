def extract_words():
    """Extract all unique words from the Bible, with frequencies and verse refs."""
    if not BIBLE_DB.exists():
        raise FileNotFoundError(f"Bible database not found: {BIBLE_DB}")

    DATA_DIR.mkdir(parents=True, exist_ok=True)

    con = sqlite3.connect(str(BIBLE_DB))
    cur = con.cursor()

    # Get book names for readable output
    book_names = {}
    cur.execute("SELECT book_id, name_ru FROM books")
    for row in cur.fetchall():
        book_names[row[0]] = row[1]

    # Read all verses
    cur.execute("SELECT book_id, chapter, verse, synodal_text FROM verses")
    verses = cur.fetchall()
    con.close()

    print(f"Read {len(verses)} verses from {len(book_names)} books")

    # Tokenize and count
    word_freq: dict[str, int] = defaultdict(int)
    word_refs: dict[str, list[tuple[str, int, int, str]]] = defaultdict(list)
    total_tokens = 0
    token_pattern = re.compile(r'[а-яёА-ЯЁ]+')

    for book_id, chapter, verse_num, text in verses:
        tokens = token_pattern.findall(text)
        total_tokens += len(tokens)
        for token in tokens:
            lower = token.lower()
            if len(lower) < MIN_WORD_LENGTH:
                continue
            word_freq[lower] += 1
            # Store up to MAX_REFS_PER_WORD references (reservoir sampling for diversity)
            refs = word_refs[lower]
            if len(refs) < MAX_REFS_PER_WORD:
                refs.append((book_id, chapter, verse_num, text))
            else:
                # Reservoir sampling: replace with decreasing probability
                j = random.randint(0, word_freq[lower] - 1)
                if j < MAX_REFS_PER_WORD:
                    refs[j] = (book_id, chapter, verse_num, text)

    unique_all = len(word_freq)
    print(f"Total tokens: {total_tokens:,}")
    print(f"Unique word forms (length >= {MIN_WORD_LENGTH}): {unique_all:,}")

    # Apply stopword filter
    filtered = {}
    stopped_count = 0
    for word, count in word_freq.items():
        if word in STOPWORDS:
            stopped_count += 1
            continue
        # Store refs as compact tuples (book_id, chapter, verse, short_text)
        refs = []
        for book_id, ch, vs, text in word_refs[word]:
            # Truncate verse text to keep JSON manageable
            short_text = text[:150] + "..." if len(text) > 150 else text
            refs.append([book_id, ch, vs, short_text])
        filtered[word] = {"count": count, "refs": refs}

    print(f"Stopwords removed: {stopped_count:,}")
    print(f"Words after filtering: {len(filtered):,}")

    # Frequency distribution
    freq_ranges = [
        (1, 1, "hapax (count=1)"),
        (2, 5, "rare (2-5)"),
        (6, 20, "uncommon (6-20)"),
        (21, 100, "moderate (21-100)"),
        (101, 500, "common (101-500)"),
        (501, 99999, "very common (500+)"),
    ]
    print("\nFrequency distribution:")
    for lo, hi, label in freq_ranges:
        n = sum(1 for w in filtered.values() if lo <= w["count"] <= hi)
        print(f"  {label}: {n:,}")

    # Save output
    output = {
        "meta": {
            "total_tokens": total_tokens,
            "unique_forms_raw": unique_all,
            "stopwords_removed": stopped_count,
            "unique_forms_filtered": len(filtered),
            "bible_db": str(BIBLE_DB),
            "book_count": len(book_names),
            "verse_count": len(verses),
        },
        "words": filtered,
    }

    with open(PASS1_OUTPUT, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=1)

    size_mb = PASS1_OUTPUT.stat().st_size / (1024 * 1024)
    print(f"\n✓ Saved {PASS1_OUTPUT} ({size_mb:.1f} MB)")
    print(f"  {len(filtered):,} words ready for Pass 2")

    return output


if __name__ == "__main__":
    extract_words()
